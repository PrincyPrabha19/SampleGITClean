        public static bool LaunchApplication(string appPath, string cmdLine, string workDir)

        {
            bool didAppLaunchSucessfully = false;

            const uint NORMAL_PRIORITY_CLASS = 0x0020;

            PROCESS_INFORMATION pInfo = new PROCESS_INFORMATION();
            STARTUPINFO psi = new STARTUPINFO();
            SECURITY_ATTRIBUTES tsecAttr = new SECURITY_ATTRIBUTES();
            SECURITY_ATTRIBUTES pSecAttr = new SECURITY_ATTRIBUTES();
            pSecAttr.Length = Marshal.SizeOf(pSecAttr);
            tsecAttr.Length = Marshal.SizeOf(tsecAttr);

            try
            {
                didAppLaunchSucessfully = CreateProcess(appPath, cmdLine, ref pSecAttr, ref tsecAttr, true, NORMAL_PRIORITY_CLASS, IntPtr.Zero, workDir, ref psi, out pInfo);

                if (!didAppLaunchSucessfully)
                {
                    throw new Exception("StartProcessAsCurrentUser: CreateProcessAsUser failed.  Error Code -" + Marshal.GetLastWin32Error());
                }
            }
            finally
            {
                CloseHandle(pInfo.hThread);
                CloseHandle(pInfo.hProcess);
            }
            



            return didAppLaunchSucessfully;
        }


        public static bool LaunchApplicationAsCurrentUser(string appPath, string cmdLine = null, string workDir = null, bool visible = true)
        {
            var hUserToken = IntPtr.Zero;
            var startInfo = new STARTUPINFO();
            var procInfo = new PROCESS_INFORMATION();
            var pEnv = IntPtr.Zero;
            int iResultOfCreateProcessAsUser;

            startInfo.cb = Marshal.SizeOf(typeof(STARTUPINFO));

            try
            {
                if (!GetSessionUserToken(ref hUserToken))
                {
                    throw new Exception("StartProcessAsCurrentUser: GetSessionUserToken failed.");
                }

                uint dwCreationFlags = CREATE_UNICODE_ENVIRONMENT | (uint)(visible ? CREATE_NEW_CONSOLE : CREATE_NO_WINDOW);
                startInfo.wShowWindow = (short)(visible ? SW.SW_SHOW : SW.SW_HIDE);
                startInfo.lpDesktop = "winsta0\\default";

                if (!CreateEnvironmentBlock(ref pEnv, hUserToken, false))
                {
                    throw new Exception("StartProcessAsCurrentUser: CreateEnvironmentBlock failed.");
                }

                if (!CreateProcessAsUser(hUserToken,
                    appPath, // Application Name
                    cmdLine, // Command Line
                    IntPtr.Zero,
                    IntPtr.Zero,
                    false,
                    dwCreationFlags,
                    pEnv,
                    workDir, // Working directory
                    ref startInfo,
                    out procInfo))
                {
                    iResultOfCreateProcessAsUser = Marshal.GetLastWin32Error();
                    throw new Exception("StartProcessAsCurrentUser: CreateProcessAsUser failed.  Error Code -" + iResultOfCreateProcessAsUser);
                }

                iResultOfCreateProcessAsUser = Marshal.GetLastWin32Error();
            }
            catch //(Exception exception)
            {
                //QuickConfigPlugin.WyseLibConfiglog.LogEWyseWin(exception, "Error in LaunchApplicationAsCurrentUser");
            }
            finally
            {
                CloseHandle(hUserToken);
                if (pEnv != IntPtr.Zero)
                {
                    DestroyEnvironmentBlock(pEnv);
                }
                CloseHandle(procInfo.hThread);
                CloseHandle(procInfo.hProcess);
            }

            return true;
        }

        public static bool GetSessionUserToken(ref IntPtr phUserToken)
        {
            var bResult = false;
            var hImpersonationToken = IntPtr.Zero;
            var activeSessionId = INVALID_SESSION_ID;
            var pSessionInfo = IntPtr.Zero;
            var sessionCount = 0;

            // Get a handle to the user access token for the current active session.
            if (WTSEnumerateSessions(WTS_CURRENT_SERVER_HANDLE, 0, 1, ref pSessionInfo, ref sessionCount) != 0)
            {
                var arrayElementSize = Marshal.SizeOf(typeof(WTS_SESSION_INFO));
                var current = pSessionInfo;

                for (var i = 0; i < sessionCount; i++)
                {
                    var si = (WTS_SESSION_INFO)Marshal.PtrToStructure((IntPtr)current, typeof(WTS_SESSION_INFO));
                    current += arrayElementSize;

                    //Console.WriteLine($"Session ID: {si.SessionID}");

                    if (si.State == WTS_CONNECTSTATE_CLASS.WTSActive)
                    {
                        activeSessionId = si.SessionID;
                        //Console.WriteLine($"Active Session ID: {activeSessionId}");
                    }
                    if (si.State == WTS_CONNECTSTATE_CLASS.WTSDisconnected)
                    {
                        activeSessionId = si.SessionID;
                        //Console.WriteLine($"Disconnected Session ID: {activeSessionId}");
                    }
                }
            }

            // If enumerating did not work, fall back to the old method
            if (activeSessionId == INVALID_SESSION_ID)
            {
                activeSessionId = WTSGetActiveConsoleSessionId();
                
            }

            if (WTSQueryUserToken(activeSessionId, ref hImpersonationToken) != 0)
            {

                //Console.WriteLine($"Impersonation token : {hImpersonationToken}");

                // Convert the impersonation token to a primary token
                bResult = DuplicateTokenEx(hImpersonationToken, LIBCONFIG_MAXIMUM_ALLOWED, IntPtr.Zero,
                    (int)SECURITY_IMPERSONATION_LEVEL.SecurityImpersonation, (int)TOKEN_TYPE.TokenPrimary,
                    ref phUserToken);

                CloseHandle(hImpersonationToken);
            }
            else if(OpenProcessToken(GetCurrentProcess(), TOKEN_DUPLICATE, ref hImpersonationToken))
            {
                bResult = DuplicateTokenEx(hImpersonationToken, LIBCONFIG_MAXIMUM_ALLOWED, IntPtr.Zero,
                    (int)SECURITY_IMPERSONATION_LEVEL.SecurityImpersonation, (int)TOKEN_TYPE.TokenPrimary,
                    ref phUserToken);
                CloseHandle(hImpersonationToken);

            }

            return bResult;
        }

        static uint GetPID(String prcoessName)
        {
            uint winlogonPid = 0;
            uint usId = WTSGetActiveConsoleSessionId();

            Process[] processes = Process.GetProcessesByName(prcoessName);
            foreach (Process allProcess in processes)
            {
                if ((uint)allProcess.SessionId == usId)
                {
                    winlogonPid = (uint)allProcess.Id;
                }
            }

            return winlogonPid;
        }
